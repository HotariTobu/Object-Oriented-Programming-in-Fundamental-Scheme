public class Author {
    // 書籍の著者の名前
    private String name;
    // 書籍の著者のメールアドレス
    private String mail;

    public Author(String name, String mail) {
        this.name = name;
        this.mail = mail;
    }

    public String getName() {
        return name;
    }

    public String getMail() {
        return mail;
    }
}

