import java.awt.Color;
import java.awt.Graphics;

import javax.swing.JFrame;
import javax.swing.JPanel;

public class Sample14e {
    public static void main(String[] args) {
        Sample14e gui = new Sample14e();
        gui.go();
    }

    // グラフがウィンドウにおさまるように
    public int ix(double x) {
        return (int)(x / 75 * 100 + 100);
    }

    public int iy(double y) {
        return (int)(500 - y / 75 * 100);
    }

    public void go() {
        JFrame frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);

        // 無名クラス
        JPanel panel = new JPanel() {
            @Override
            protected void paintComponent(Graphics g) {
                super.paintComponent(g);

                draw(g);
            };
        };

        frame.getContentPane().add(panel);
        frame.setSize(600, 700);
        frame.setVisible(true);
    }

    public void draw(Graphics g) {
        // x軸とy軸を描画
        g.drawLine(ix(0), iy(-100), ix(0), iy(100));
        g.drawLine(ix(-100), iy(0), ix(100), iy(0));

        double x0 = 20;
        double y0 = 10;

        int d = 5;

        // 初期値に印をつける
        g.drawLine(ix(x0 - d), iy(y0), ix(x0 + d), iy(y0));
        g.drawLine(ix(x0), iy(y0 - d), ix(x0), iy(y0 + d));

        g.setColor(Color.red);

        double x = x0;
        double y = y0;

        double h = 0.02;

        double t = 0;
        int n = 300;

        for (int i = 0; i < n; i++) {
            double xx = x + h * (y - 40);
            double yy = y + h * (-x + 50);

            g.drawLine(ix(x), iy(y), ix(xx), iy(yy));

            t += h;
            x = xx;
            y = yy;

            System.out.println("i=" + i + " t=" + t + " x=" + x + " y=" + y);
        }
    }
}

